            <div id="welcome" style="border:0px"><h1>Über histat</h1><br/>
                <h3>Datenbank zur historischen Statistik</h3>
                <p class="normal">
                    Mit dem Datenbanksystem histat stellen wir hier ein Produkt zur Verfügung, das seit 2004 in der GESIS von Jürgen Sensch, Gabriele Franzmann und Rainer Metz entwickelt und vorangetrieben wurde. Die Online-Plattform hat die Funktion, Zeitreihen aus der historischen, wirtschafts- und sozialwissenschaftlichen Forschung zugänglich zu machen. Der Zugriff auf die Datenbestände sollte auf der einen Seite möglichst einfach erfolgen, auf der anderen Seite wissenschaftlichen Ansprüchen genügen und die angebotenen Zeitreihen in ihrem Entstehungskontext hinreichend dokumentieren. 
                    Aus dieser Idee ist ein erfolgreiches Produkt geworden. Dieses Jahr werden wir über 3.000 registrierte Nutzerinnen und Nutzer aus der ganzen Welt verzeichnen. Die Datenbank umfasst mittlerweile über eine Viertelmillion Zeitreihen aus über 360 Studien. Im letzten Jahr wurden über 70.000 Zeitreihen heruntergeladen. Monatlich werden mehrere Studien ergänzt. 
                    Auch organisatorisch wurde histat auf eine solide Grundlage gesetzt. Mit der Auflösung des Zentrums für Historische Sozialforschung und der Etablierung einer Teamstruktur innerhalb von GESIS wird die Datenbank dauerhaft vom <a href="http://www.gesis.org/unser-angebot/daten-analysieren/daten-historische-studien/">Team Datenservice Historische Studien</a> betreut und weiterentwickelt. Innerhalb des zurzeit zehnköpfigen Teams widmen sich drei Mitarbeiterinnen und Mitarbeiter unterstützt von mehreren Hilfskräften der kontinuierlichen Bestandserweiterung und –pflege.  
                    2011 stellte die GESIS Mittel für einen umfassenden Relaunch bereit. Mit diesen Mitteln wurde die Oberfläche grundlegend überarbeitet und eine einheitliche Benutzerführung über alle Funktionen geschaffen. 
                </p>
                 <br><br>
                <h3>Inhalt</h3>
                <p class="normal">
                    Da histat von Beginn an als System für heterogene Datenbestände konzipiert war,  variiert sowohl die Anzahl der Zeitreihen pro Studie als auch die abgedeckte Zeitspanne beträchtlich. Rund zwanzig Prozent der Studien beinhalten bis zu 50 Zeitreihen, weitere 13 Prozent bis zu 100, 40 Prozent zwischen 101 und 500. Jeweils gut zehn Prozent beinhalten 501 bis 1000 und 1001 bis 5000 Zeitreihen. Neun Studien (3 Prozent) umfassen mehr als 5.000 Zeitreihen, die größten drei davon alleine insgesamt 61.274 Zeitreihen.

Die folgende Abbildung zeigt die Anzahl der Zeitreihen pro Studie (in logarithmischer Skalierung) sowie ihre Anfangs- und Endzeitpunkte. Man sieht, dass eine ganze Reihe von Studien auch sehr lange Zeiträume von mehreren hundert Jahren umfassen, auch solche mit relativ vielen Zeitreihen. Der Schwerpunkt (dunkle Färbung) liegt jedoch eindeutig in dem Bereich von etwa 1850 bis 1950 und zwischen 100 und 1.000 Zeitreihen. 
</p>
